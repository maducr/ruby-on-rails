# RELACIONAIS #
# >, >=, <, <=, ==, !=
=begin
> - maior que
>= - maior ou igual
< - menor que
<= - menor ou igual
== - igual
!= - diferente que
=end
if 1 < 2
    puts 'a'
else 
    puts 'b'
end

# ARITÉTICOS #
# +, -, *, /, **, %
=begin
+ - soma
- - subtração
* - multiplicação
/ - divisão
** - potência
% - módulo
=end
if 1 ** 2
    puts 'a'
else 
    puts 'b'
end