# AMBIENTES DO RAILS #

# Por parão, o Rails vem com 3 ambientes: Desenvolvimento, Teste e Produção

# Esses ambientes possibilitam ter configurações isoladas para cada ambiente

# A Gemfile é a forma mais simplies de perceber isso

# https://guides.rubyonrails.org/configuring.html#creating-rails-environments

# Rodar o ambiente de produção: RAILS_ENV=production rails s ou rails s -e production