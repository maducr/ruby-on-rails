# R ails dbconsole - é o co,amdo usado se conectar ao BD e executar comandos para einspecioná-lo. (rails db)

# R ails Tasks - são tarefas predefinidas que o Rails pode executar (rails -T)
# - Para fultrar tasks específicas use as inicias do que procura (rails -T db)
# Tasks de banco de dados mais usados:
# - rails db:create #cria o db
# - rails db:drop #apaga o db
# - rails db:migrate #executa as migrations
# - rails db:rollback #desfaz a última migration